package Lec_02;

public class Integer_check {
    public static void main(String[]args){
        int n = 200;
        if (n > 0) {
            System.out.println(n + " is Positive");
        } else if (n < 0) {
            System.out.println(n + " is Negative");
        } else System.out.println(n + " is Zero");
    }


}

